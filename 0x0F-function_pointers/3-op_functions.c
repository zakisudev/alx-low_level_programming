/**
 * op_add - adds two ints, a and b
 * @a: First argument
 * @b: Second argument
 *
 * Return: the sum of the two ints
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two ints, a and b
 * @a: First argument
 * @b: Second argument
 *
 * Return: the difference of the two ints
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two ints, a and b
 * @a: First argument
 * @b: Second argument
 *
 * Return: the product of the two ints
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two ints, a and b
 * @a: First argument
 * @b: Second argument
 *
 * Return: the quotient of the two ints
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - divides two ints, a and b, and outputs the remainder
 * @a: First argument
 * @b: Second argument
 *
 * Return: the remainder from the two ints division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
